#' Cereal data
#'
#' @docType data
#'
#' @details This data measures different nutritional values of all
#' all the brands of cold cereal along with the shelf the cereal
#' occupies in the store.
#'
#' @usage data(Cereal)
#'
#' @format The variables are
#'
#' - `name`: The brand name of the cereal.
#' - `calories`: Number of calories per serving.
#' - `protein`: Number of grams of protein per serving.
#' - `fat`: Number of grams of fat per serving.
#' - `sodium`: Milligrams of sodium per serving.
#' - `fiber`: Number of grams of dietary fiber per serving.
#' - `carbo`: Number of grams of complex carbohydrates per serving.
#' - `sugars`: Number of grams of sugar per serving.
#' - `potass`: Milligrams of potassium per serving.
#' - `shelf`: Display shelf counting from the floor.  A higher number is a higher shelf.
#'
#'
#' @keywords datasets
#'
#' @source Each company's website.
