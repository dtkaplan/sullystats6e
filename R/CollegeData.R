#' Payscale Return on Investment in College Data
#'
#' @docType data
#' @details  This is data provided by `payscale.com` that
#' measures various variables that may be used to judge the
#' monetary return on an investment in a college education.
#'
#'
#' @usage data(CollegeData)
#'
#' @format  The variables are
#'
#' - `Rank`: Rank of the school in terms of ROI.
#' - `Name`: Name of the school.
#' - `ROI`: Twenty year net return on investement.  It is the difference between 20 year median pay for a bachelor's grad and 24 year median pay for a high school grad minus total four-year cost.
#' - `Cost`: Full cost, including tuition and fees, room & board, and books&supplies.
#' - `GradRate`: Percent of full-time, first-time degree seeking undergraduate students who began their studies at the given school and graduate within six years.
#' - `Years`: Number of years it takes for at least 65 percent of graduates from the school to receive their diplomas.
#' - `Loan`: Average loan amount for full-time,first-time degree-seeking undgraduate students.
#'
#' @keywords datasets
#'
#' @source  [payscale.com/college-roi#roi-list-bottom-content](payscale.com/college-roi#roi-list-bottom-content)
#'
"CollegeData"
